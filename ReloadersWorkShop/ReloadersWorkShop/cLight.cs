using System;

//============================================================================*
// Namespace
//============================================================================*

namespace ReloadersWorkShop
	{
	//============================================================================*
	// cLight Class
	//============================================================================*

	[Serializable]
	public partial class cLight : cGear
		{
		//============================================================================*
		// Private Data Members
		//============================================================================*

		private int m_nLumens = 0;

		private string m_strBattery = "";

		//============================================================================*
		// cLight() - Constructor
		//============================================================================*

		public cLight()
			: base(cGear.eGearTypes.Light)
			{
			}

		//============================================================================*
		// cLight() - Copy Constructor
		//============================================================================*

		public cLight(cLight Light)
			: base(Light)
			{
			Copy(Light);
			}

		//============================================================================*
		// Battery Property
		//============================================================================*

		public string Battery
			{
			get
				{
				return (m_strBattery);
				}

			set
				{
				m_strBattery = value;
				}
			}

		//============================================================================*
		// Copy()
		//============================================================================*

		public override void Copy(cGear Gear)
			{
			if (Gear.GearType != eGearTypes.Light)
				return;

			base.Copy(Gear);

			cLight Light = (cLight)Gear;

			m_nLumens = Light.m_nLumens;

			m_strBattery = Light.m_strBattery;
			}

		//============================================================================*
		// Lumens Property
		//============================================================================*

		public int Lumens
			{
			get
				{
				return (m_nLumens);
				}

			set
				{
				m_nLumens = value;
				}
			}

		//============================================================================*
		// ToString()
		//============================================================================*

		public override string ToString()
			{
			return(String.Format("{0} {1} - [2} Lumen Light", Manufacturer.Name, PartNumber, m_nLumens));
			}
		}
	}
