using System;

//============================================================================*
// Namespace
//============================================================================*

namespace ReloadersWorkShop
	{
	//============================================================================*
	// cPrintColumn Class
	//============================================================================*

	public class cPrintColumn
		{
		//============================================================================*
		// Private Data Members
		//============================================================================*

		private string m_strName = "";
		private float m_dWidth = 0;

		//============================================================================*
		// cPrintColumn() - Constructor
		//============================================================================*

		public cPrintColumn(string strName)
			{
			m_strName = strName;
			}

		//============================================================================*
		// Name Property
		//============================================================================*

		public string Name
			{
			get { return (m_strName); }
			set { m_strName = value; }
			}

		//============================================================================*
		// Width Property
		//============================================================================*

		public float Width
			{
			get { return (m_dWidth); }
			set { m_dWidth = value; }
			}
		}
	}
