using System;
using System.Drawing;
using System.Windows;
using System.Windows.Forms;

//============================================================================*
// NameSpace
//============================================================================*

namespace ReloadersWorkShop.Preferences
	{
	//============================================================================*
	// cColumn class
	//============================================================================*

	[Serializable]
	public class cColumn
		{
		//----------------------------------------------------------------------------*
		// Private Data Members
		//----------------------------------------------------------------------------*

		private cPreferences.eApplicationListView m_eListView;
		private string m_strName = null;

		private int m_nDisplayIndex = -1;
		private int m_nWidth = -1;

		//============================================================================*
		// cColumn() - Constructor
		//============================================================================*

		public cColumn()
			{

			}

		//============================================================================*
		// DisplayIndex Property
		//============================================================================*

		public int DisplayIndex
			{
			get { return (m_nDisplayIndex); }
			set { m_nDisplayIndex = value; }
			}

		//============================================================================*
		// ListView Property
		//============================================================================*

		public cPreferences.eApplicationListView ListView
			{
			get { return (m_eListView); }
			set { m_eListView = value; }
			}

		//============================================================================*
		// Name Property
		//============================================================================*

		public string Name
			{
			get { return (m_strName); }
			set { m_strName = value; }
			}

		//============================================================================*
		// Width Property
		//============================================================================*

		public int Width
			{
			get { return (m_nWidth); }
			set { m_nWidth = value; }
			}
		}
	}
