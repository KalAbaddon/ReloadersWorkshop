using System.Windows.Forms;

//============================================================================*
// Namespace
//============================================================================*

namespace CommonLib.Controls
	{
	//============================================================================*
	// cCancelButton Class
	//============================================================================*

	public class cCancelButton : cButton
		{
		//============================================================================*
		// Public Enumerations
		//============================================================================*

		public enum eButtonTypes
			{
			Cancel = 0,
			Close
			}

		//============================================================================*
		// Private Data Members
		//============================================================================*

		private eButtonTypes m_eType = eButtonTypes.Cancel;

		//============================================================================*
		// cCancelButton() - Constructor
		//============================================================================*

		public cCancelButton()
			{
			ButtonType = eButtonTypes.Cancel;

			DialogResult = DialogResult.Cancel;
			}

		//============================================================================*
		// ButtonType Property
		//============================================================================*

		public eButtonTypes ButtonType
			{
			get
				{
				return (m_eType);
				}
			set
				{
				m_eType = value;

				SetText();
				}
			}

		//============================================================================*
		// SetText()
		//============================================================================*

		public void SetText()
			{
			switch (ButtonType)
				{
				case eButtonTypes.Cancel:
					Text = "Cancel";

					ToolTip = "Click to cancel changes and exit.";

					break;
				case eButtonTypes.Close:
					Text = "Close";

					ToolTip = "Click to close dialog and return.";

					break;
				}
			}
		}
	}
