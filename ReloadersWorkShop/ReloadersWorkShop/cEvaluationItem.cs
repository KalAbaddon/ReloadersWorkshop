using System;

//============================================================================*
// Namespace
//============================================================================*

namespace ReloadersWorkShop
	{
	//============================================================================*
	// cEvaluationItem Class
	//============================================================================*

	class cEvaluationItem
		{
		//============================================================================*
		// Private Data Members
		//============================================================================*

		private cLoad m_Load = null;
		private cCharge m_Charge = null;
		private cChargeTest m_ChargeTest = null;

		//============================================================================*
		// cEvaluationItem() - Constructor
		//============================================================================*

		public cEvaluationItem(cLoad Load, cCharge Charge, cChargeTest ChargeTest)
			{
			m_Load = Load;
			m_Charge = Charge;
			m_ChargeTest = ChargeTest;
			}

		//============================================================================*
		// Charge Property
		//============================================================================*

		public cCharge Charge
			{
			get
				{
				return (m_Charge);
				}

			set
				{
				m_Charge = value;
				}
			}

		//============================================================================*
		// ChargeTest Property
		//============================================================================*

		public cChargeTest ChargeTest
			{
			get
				{
				return (m_ChargeTest);
				}

			set
				{
				m_ChargeTest = value;
				}
			}

		//============================================================================*
		// Load Property
		//============================================================================*

		public cLoad Load
			{
			get
				{
				return (m_Load);
				}

			set
				{
				m_Load = value;
				}
			}
		}
	}
